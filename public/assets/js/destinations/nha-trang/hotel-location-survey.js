function closeSurveyToStart(){ currentQuestionIndex=0; answers=new Array(cityConfig.questions.length).fill(null); resultView?.classList.remove("is-active"); surveyWrap?.classList.remove("is-result-mode"); surveyWrap?.classList.remove("is-survey-started"); locationPage?.classList.remove("is-result-mode"); locationPage?.classList.remove("is-survey-started"); surveyView.style.display="block"; renderQuestion(); window.scrollTo({top:0,behavior:"smooth"}); }
