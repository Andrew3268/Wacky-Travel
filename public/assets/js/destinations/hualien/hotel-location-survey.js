(function () {
  const $ = (id) => document.getElementById(id);
  const startBtn = $('startSurveyBtn');
  const surveyView = $('surveyView');
  const resultView = $('resultView');
  const questionTitle = $('questionTitle');
  const questionHelp = $('questionHelp');
  const questionCount = $('questionCount');
  const progressText = $('progressText');
  const progressBar = $('progressBar');
  const progressFill = $('progressFill');
  const optionsArea = $('optionsArea');
  const prevBtn = $('prevBtn');
  const nextBtn = $('nextBtn');
  const resetBtn = $('resetBtn');
  const backBtn = $('backBtn');
  let current = 0;
  let answers = new Array(cityConfig.questions.length).fill(null);

  const escapeHtml = (value) => String(value || '').replace(/[&<>'"]/g, (ch) => ({ '&': '&amp;', '<': '&lt;', '>': '&gt;', "'": '&#39;', '"': '&quot;' }[ch]));
  function setProgress() {
    const total = cityConfig.questions.length;
    const percent = Math.round(((current + 1) / total) * 100);
    questionCount.textContent = `${current + 1} / ${total}`;
    progressText.textContent = current < total - 1 ? '조건을 하나씩 좁히는 중입니다' : '이제 결과를 볼 수 있어요';
    progressBar.setAttribute('aria-valuenow', String(percent));
    progressFill.style.width = `${percent}%`;
  }
  function renderQuestion() {
    const q = cityConfig.questions[current];
    setProgress();
    questionTitle.textContent = q.title;
    questionHelp.textContent = q.help || '';
    optionsArea.innerHTML = q.options.map((option, index) => `
      <button class="wt-option ${answers[current] === index ? 'is-selected' : ''}" type="button" data-option-index="${index}">
        <strong>${escapeHtml(option.title)}</strong>
        ${option.desc ? `<span>${escapeHtml(option.desc)}</span>` : ''}
      </button>
    `).join('');
    prevBtn.hidden = current === 0;
    nextBtn.disabled = answers[current] === null;
    nextBtn.textContent = current === cityConfig.questions.length - 1 ? '결과 보기' : '다음';
  }
  function calculateScores() {
    const scores = {};
    Object.keys(cityConfig.areas).forEach((key) => { scores[key] = 0; });
    answers.forEach((answerIndex, qIndex) => {
      if (answerIndex === null) return;
      const option = cityConfig.questions[qIndex].options[answerIndex];
      Object.entries(option.scores || {}).forEach(([key, score]) => {
        if (Object.prototype.hasOwnProperty.call(scores, key)) scores[key] += Number(score || 0);
      });
    });
    return Object.entries(scores).map(([key, score]) => ({ key, score, ...cityConfig.areas[key] })).sort((a, b) => b.score - a.score);
  }
  function renderResult() {
    const ranked = calculateScores();
    const top = ranked[0];
    const second = ranked[1];
    $('resultTitle').textContent = top.name;
    $('resultSummary').textContent = top.summary;
    $('resultLeadTitle').textContent = top.leadTitle || top.name;
    $('resultLeadText').textContent = top.leadText || top.lead || top.summary;
    $('resultWhyText').textContent = `${top.name}은(는) 이번 답변에서 가장 높은 점수를 받은 권역입니다. ${second ? `차선책으로는 ${second.name}도 함께 비교해보세요.` : ''}`;
    const reasons = [
      ['이번 일정과 맞는 이유', top.compareGood || top.label || top.summary],
      ['예약 전 확인할 점', top.compareCaution || '숙소 주변 이동 방식과 밤 동선을 함께 확인하세요.'],
      ['비교해볼 대안', second ? `${second.name}은(는) 일정이 조금 달라질 때 함께 볼 만한 대안입니다.` : '다른 권역도 일정 성격에 맞춰 함께 비교해보세요.']
    ];
    $('reasonCardList').innerHTML = reasons.map(([title, text]) => `<article class="wt-reason-card"><h4>${escapeHtml(title)}</h4><p>${escapeHtml(text)}</p></article>`).join('');
    $('decisionConclusionTitle').textContent = '한 줄 결론';
    $('decisionConclusionText').textContent = top.decision || top.label || top.summary;
    $('hotelSectionTitle').textContent = `${top.name}에서 먼저 비교할 만한 호텔`;
    $('hotelSectionDesc').textContent = '실제 예약 전에는 가격, 객실 타입, 취소 조건, 최근 후기를 함께 확인하세요.';
    $('hotelCardList').innerHTML = (top.hotels || []).slice(0, 5).map((hotel) => `
      <article class="wt-hotel-card"><div class="wt-hotel-card__body">
        <div class="wt-hotel-card__topline"><div class="wt-hotel-card__fit">${escapeHtml(hotel.tag)}</div><span class="wt-hotel-card__stars">성급 정보</span></div>
        ${hotel.englishName ? `<div class="wt-hotel-card__subname">${escapeHtml(hotel.englishName)}</div>` : ''}
        <h3>${escapeHtml(hotel.name)}</h3><div class="wt-hotel-card__point">${escapeHtml(hotel.location)}</div><p>${escapeHtml(hotel.reason)}</p>
        <div class="wt-hotel-actions"><a class="wt-btn wt-btn--primary" href="${escapeHtml(hotel.url || '#')}" rel="sponsored noopener">잔여 객실 확인</a></div>
      </div></article>
    `).join('');
    $('relatedPostTitle').textContent = `${top.name} 숙소 선택을 더 좁혀보세요`;
    $('relatedPostDesc').textContent = '구역별 추천 글과 화렌 호텔 위치 가이드를 함께 보면 선택이 더 쉬워집니다.';
    $('relatedPostList').innerHTML = (top.links || []).map((link) => `<li><a href="${escapeHtml(link.url)}">${escapeHtml(link.title)}</a></li>`).join('');
    surveyView.hidden = true;
    resultView.classList.add('is-visible');
    resultView.hidden = false;
    window.scrollTo({ top: 0, behavior: 'smooth' });
  }
  optionsArea?.addEventListener('click', (event) => {
    const button = event.target.closest('[data-option-index]');
    if (!button) return;
    answers[current] = Number(button.dataset.optionIndex);
    renderQuestion();
  });
  startBtn?.addEventListener('click', () => { renderQuestion(); surveyView.classList.add('is-visible'); surveyView.hidden = false; startBtn.closest('.wt-survey-start')?.setAttribute('hidden', ''); });
  prevBtn?.addEventListener('click', () => { if (current > 0) { current -= 1; renderQuestion(); } });
  nextBtn?.addEventListener('click', () => { if (answers[current] === null) return; if (current < cityConfig.questions.length - 1) { current += 1; renderQuestion(); } else { renderResult(); } });
  resetBtn?.addEventListener('click', () => { current = 0; answers = new Array(cityConfig.questions.length).fill(null); resultView.hidden = true; resultView.classList.remove('is-visible'); surveyView.hidden = false; surveyView.classList.add('is-visible'); renderQuestion(); });
  backBtn?.addEventListener('click', () => { window.location.href = '/destinations/hualien/'; });
  surveyView.hidden = true;
  resultView.hidden = true;
})();
