function getProgressMessage(){ const step=currentQuestionIndex+1,total=cityConfig.questions.length; if(step===1)return"가볍게 시작해볼까요?"; if(step===total)return"마지막 질문이에요!"; if(step>=total-1)return"거의 다 왔어요"; if(step>=Math.ceil(total*.55))return"좋아요, 취향이 보이기 시작해요"; return"동선을 하나씩 맞춰보는 중이에요"; }
function renderQuestion(){ const q=cityConfig.questions[currentQuestionIndex]; const selected=answers[currentQuestionIndex]; const answered=answers.filter((a)=>a!==null).length; const percent=Math.round((answered/cityConfig.questions.length)*100); questionCount.textContent=`${currentQuestionIndex+1} / ${cityConfig.questions.length}`; progressText.textContent=getProgressMessage(); progressFill.style.width=`${percent}%`; progressBar.setAttribute("aria-valuenow", String(percent)); questionTitle.textContent=q.title; questionHelp.textContent=q.help; optionsArea.innerHTML=""; q.options.forEach((opt,i)=>{ const b=document.createElement("button"); b.type="button"; b.className="wt-option"; b.setAttribute("aria-pressed", selected===i?"true":"false"); if(selected===i)b.classList.add("is-selected"); b.innerHTML=`<span class="wt-option-title">${escapeHtml(opt.title)}</span>`; b.addEventListener("click",()=>{answers[currentQuestionIndex]=i; renderQuestion();}); optionsArea.appendChild(b); }); prevBtn.disabled=false; prevBtn.textContent=currentQuestionIndex===0?"닫기":"이전"; nextBtn.disabled=selected===null; nextBtn.textContent=currentQuestionIndex===cityConfig.questions.length-1?"결과 보기":"다음"; }
